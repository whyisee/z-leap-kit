function getLanguage() {
  const configured = getConfiguredLanguage();
  if (configured === LANGUAGE_EN || configured === LANGUAGE_ZH) {
    return configured;
  }
  return resolveAutoLanguage();
}

function resolveAutoLanguage() {
  const envLanguage = String(vscode.env && vscode.env.language || '').toLowerCase();
  return envLanguage.startsWith('zh') ? LANGUAGE_ZH : LANGUAGE_EN;
}

function getConfiguredLanguage() {
  const configured = vscode.workspace && typeof vscode.workspace.getConfiguration === 'function'
    ? vscode.workspace.getConfiguration('leapHome').get('language', LANGUAGE_AUTO)
    : LANGUAGE_AUTO;
  return [LANGUAGE_AUTO, LANGUAGE_ZH, LANGUAGE_EN].includes(configured) ? configured : LANGUAGE_AUTO;
}

function isEnglish(locale = getLanguage()) {
  return locale === LANGUAGE_EN;
}

function translateText(value, locale = getLanguage()) {
  const text = String(value || '');
  if (!text || !isEnglish(locale)) {
    return text;
  }
  if (Object.prototype.hasOwnProperty.call(EN_TEXT, text)) {
    return EN_TEXT[text];
  }
  let translated = text;
  const keys = Object.keys(EN_TEXT)
    .filter((key) => key && key.length >= 4 && text.includes(key))
    .sort((left, right) => right.length - left.length);
  for (const key of keys) {
    translated = translated.split(key).join(EN_TEXT[key]);
  }
  return translated;
}

function localizeObject(value, locale = getLanguage()) {
  if (Array.isArray(value)) {
    return value.map((item) => localizeObject(item, locale));
  }
  if (value && typeof value === 'object') {
    const result = {};
    for (const [key, item] of Object.entries(value)) {
      result[key] = ['title', 'description', 'label', 'detail', 'activeTemplateTitle'].includes(key)
        ? translateText(item, locale)
        : localizeObject(item, locale);
    }
    return result;
  }
  return value;
}

function t(key, locale = getLanguage()) {
  return translateText(key, locale);
}

function webviewI18n(locale = getLanguage()) {
  return {
    locale,
    sourceLocale: LANGUAGE_ZH,
    messages: EN_TEXT
  };
}

